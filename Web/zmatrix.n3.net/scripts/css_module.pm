use strict;

##############################################################################
# set keyword char to /*# - allows normal C pre-processor stuff through
##############################################################################
my $keychar = "/*#";
Filepp::SetKeywordchar($keychar);

##############################################################################
# set optional line end char to something other than ""
##############################################################################
Filepp::SetOptLineEndchar("*/");

return 1;

